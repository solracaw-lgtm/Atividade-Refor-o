// Operadores Básicos

// 1. Crie duas variáveis com números diferentes
let numero1 = 10;
let numero2 = 4;

// A soma
console.log("Soma:", numero1 + numero2);

// A subtração
console.log("Subtração:", numero1 - numero2);

// A multiplicação
console.log("Multiplicação:", numero1 * numero2);

// A divisão
console.log("Divisão:", numero1 / numero2);

// O resto da divisão (módulo)
console.log("Resto da divisão (módulo):", numero1 % numero2);
